/// Initialise the glyph engine. Safe to call multiple times.
pub fn init() {
    let mmio_present = probe_font_mmio();
    let mut st = STATE.lock();
    st.default_style = TextStyle::default_amber();
    st.initialized = true;
    drop(st);
    if mmio_present {
        serial_println!("[glyph] DAVA font register detected at MMIO 0x80000010");
    }
    serial_println!("[glyph] ANIMA glyph engine online — 95 chars 8x16");
}

/// Render a byte slice as ASCII characters at (x, y) with explicit colours.
pub fn render_str(s: &[u8], x: u16, y: u16, fg: u32, bg: u32) {
    let style = TextStyle { fg, bg, scale: 1, transparent_bg: false };
    let glyph_stride = GLYPH_W as u16;
    let mut st = STATE.lock();
    for (i, &ch) in s.iter().enumerate() {
        let cx = x.saturating_add((i as u16).saturating_mul(glyph_stride));
        unsafe { render_glyph(ch, cx, y, &style); }
        st.glyphs_rendered = st.glyphs_rendered.saturating_add(1);
        st.cursor_x = cx.saturating_add(glyph_stride);
        st._window_glyphs = st._window_glyphs.saturating_add(1);
    }
    st.cursor_y = y;
}

/// Render with explicit scale factor.
pub fn render_str_scaled(s: &[u8], x: u16, y: u16, fg: u32, bg: u32, scale: u8) {
    let effective_scale = if scale == 2 { 2u8 } else { 1u8 };
    let style = TextStyle { fg, bg, scale: effective_scale, transparent_bg: false };
    let glyph_stride = (GLYPH_W as u16).saturating_mul(effective_scale as u16);
    let mut st = STATE.lock();
    for (i, &ch) in s.iter().enumerate() {
        let cx = x.saturating_add((i as u16).saturating_mul(glyph_stride));
        unsafe { render_glyph(ch, cx, y, &style); }
        st.glyphs_rendered = st.glyphs_rendered.saturating_add(1);
        st.cursor_x = cx.saturating_add(glyph_stride);
        st._window_glyphs = st._window_glyphs.saturating_add(1);
    }
    st.cursor_y = y;
}

/// Render using the engine's current default style.
pub fn print_at(s: &[u8], x: u16, y: u16) {
    let style = {
        let st = STATE.lock();
        st.default_style
    };
    let glyph_stride = (GLYPH_W as u16).saturating_mul(style.scale as u16);
    let mut st = STATE.lock();
    for (i, &ch) in s.iter().enumerate() {
        let cx = x.saturating_add((i as u16).saturating_mul(glyph_stride));
        unsafe { render_glyph(ch, cx, y, &style); }
        st.glyphs_rendered = st.glyphs_rendered.saturating_add(1);
        st.cursor_x = cx.saturating_add(glyph_stride);
        st._window_glyphs = st._window_glyphs.saturating_add(1);
    }
    st.cursor_y = y;
}

// ---------------------------------------------------------------------------
// Life pipeline tick
// ---------------------------------------------------------------------------

/// Called every life tick. `consciousness` from autopoiesis gate; `age` is
/// the organism's absolute tick counter.
pub fn tick(consciousness: u16, age: u32) {
    let _ = consciousness; // influences future quality curve if needed
    let mut st = STATE.lock();

    if !st.initialized {
        return;
    }

    // Render quality grows towards 1000 — textual mastery accumulates.
    st.render_quality = st.render_quality.saturating_add(1).min(1000);

    st._tick_counter = st._tick_counter.saturating_add(1);

    // Every 600 ticks refresh the chars/sec estimate and log.
    if st._tick_counter >= 600 {
        // chars_per_sec ≈ glyphs drawn this window / 600 ticks.
        // Dividing by 600 gives a per-tick rate; we store it as u16.
        let rate = if st._window_glyphs > 0 {
            (st._window_glyphs / 600).min(0xFFFF as u32) as u16
        } else {
            0
        };
        st.chars_per_sec = rate;
        let rendered = st.glyphs_rendered;
        let quality = st.render_quality;
        st._tick_counter = 0;
        st._window_glyphs = 0;
        drop(st);
        serial_println!("[glyph] age={} rendered={} quality={}", age, rendered, quality);
    }
}

