/// Write a 32-bit ARGB pixel at (px, py). Bounds-checked; no-ops if out of range.
#[inline(always)]
unsafe fn fb_write(px: u16, py: u16, color: u32) {
    if px >= FB_WIDTH || py >= FB_HEIGHT {
        return;
    }
    let offset = (py as u32)
        .saturating_mul(FB_STRIDE)
        .saturating_add((px as u32).saturating_mul(4)) as usize;
    let ptr = (FB_BASE + offset) as *mut u32;
    ptr.write_volatile(color);
}

// ---------------------------------------------------------------------------
// Core rasterizer — render a single glyph at (x, y)
// ---------------------------------------------------------------------------

/// Render one character onto the framebuffer.
///
/// # Safety
/// Writes directly to physical memory mapped at FB_BASE. The caller must
/// ensure the framebuffer is mapped and the coordinates are sane.
pub unsafe fn render_glyph(ch: u8, x: u16, y: u16, style: &TextStyle) {
    // Clamp to printable range; fall back to space for anything out of range.
    let idx = if ch >= 0x20 && ch <= 0x7E {
        (ch - 0x20) as usize
    } else {
        0 // space
    };

    let bitmap = &FONT_DATA[idx];
    let scale = if style.scale == 2 { 2u16 } else { 1u16 };

    for row in 0..GLYPH_H {
        let bits = bitmap[row];
        for col in 0..GLYPH_W {
            let pixel_on = (bits >> (7 - col)) & 1 != 0;
            let color = if pixel_on {
                style.fg
            } else if !style.transparent_bg {
                style.bg
            } else {
                continue; // transparent background — skip background pixels
            };

            let draw_x = x.saturating_add((col as u16).saturating_mul(scale));
            let draw_y = y.saturating_add((row as u16).saturating_mul(scale));

            if scale == 2 {
                // 2×2 block
                fb_write(draw_x,                   draw_y,                   color);
                fb_write(draw_x.saturating_add(1), draw_y,                   color);
                fb_write(draw_x,                   draw_y.saturating_add(1), color);
                fb_write(draw_x.saturating_add(1), draw_y.saturating_add(1), color);
            } else {
                fb_write(draw_x, draw_y, color);
            }
        }
    }
}

// ---------------------------------------------------------------------------
// Probe FONT_MMIO (read once; ignore if not mapped — no panic)
// ---------------------------------------------------------------------------

fn probe_font_mmio() -> bool {
    // Attempt a volatile read from DAVA's font register.
    // If the region is not mapped the read will return garbage; we check for
    // a magic sentinel (0xDAFA_F077 — "DAVA FONT"). If it matches we could
    // in principle load an alternate glyph table; for now we always use our
    // built-in font and treat MMIO as advisory only.
    let val = unsafe { (FONT_MMIO as *const u32).read_volatile() };
    val == 0xDAFA_F077
}

